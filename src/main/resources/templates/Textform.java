package templates;

public class Textform {

	private String title = "";
	private String textbox = "";
	
	public String gettitle() {
		return title;
	}
	public void settitle(String title) {
		this.title = title;
	}
	public String gettextbox() {
		return textbox;
	}
	public void settextbox(String textbox) {
		this.textbox = textbox;
	}
	
}
